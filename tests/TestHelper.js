function shuffle(a) {
  for (let i = a.length; i; i -= 1) {
    const j = Math.floor(Math.random() * i);
    // eslint-disable-next-line no-param-reassign
    [a[i - 1], a[j]] = [a[j], a[i - 1]];
  }
}

